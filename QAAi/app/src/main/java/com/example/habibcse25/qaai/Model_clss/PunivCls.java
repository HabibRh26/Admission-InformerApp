public class PunivCls {
    private int imgId;
    private String varsityName;

    public PunivCls(int imgId, String varsityName) {
        this.imgId = imgId;
        this.varsityName = varsityName;
    }

    public int getImgId() {
        return imgId;
    }

    public void setImgId(int imgId) {
        this.imgId = imgId;
    }

    public String getVarsityName() {
        return varsityName;
    }

    public void setVarsityName(String varsityName) {
        this.varsityName = varsityName;
    }
}
